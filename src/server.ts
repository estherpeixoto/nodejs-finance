import fastifyCors from '@fastify/cors'
import fastifySwagger from '@fastify/swagger'
import fastifySwaggerUi from '@fastify/swagger-ui'
import fastify from 'fastify'
import {
  type ZodTypeProvider,
  jsonSchemaTransform,
  serializerCompiler,
  validatorCompiler,
} from 'fastify-type-provider-zod'
import { env } from './env'
import { createSubscriptionRoute } from './routes/create-subscription-route'

const app = fastify().withTypeProvider<ZodTypeProvider>()

app.setValidatorCompiler(validatorCompiler)
app.setSerializerCompiler(serializerCompiler)

app.register(fastifyCors, {
  origin: env.FRONTEND_URL,
})

app.register(fastifySwagger, {
  openapi: {
    info: {
      title: 'Subscription Manager',
      version: '1.0.0',
    },
  },
  transform: jsonSchemaTransform,
})

app.register(fastifySwaggerUi, {
  routePrefix: '/docs',
})

app.register(createSubscriptionRoute)

app.listen({ port: env.BACKEND_PORT }).then(() => {
  console.log(`HTTP server running at http://localhost:${env.BACKEND_PORT}`)
})
